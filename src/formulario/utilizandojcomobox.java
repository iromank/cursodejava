    private void cbox_provinciasKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cbox_provinciasKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){
              String provincia = cbox_provincias.getSelectedItem().toString();
        switch (provincia) {
            case "corongo":
                cbox_distrito.removeAllItems();
                cbox_distrito.addItem("bambas");
                cbox_distrito.addItem("janac");
                cbox_distrito.addItem("pampa");
                cbox_distrito.addItem("yupan");

                break;
            case "pallasca":
                cbox_distrito.removeAllItems();
                cbox_distrito.addItem("santa rosa");
                cbox_distrito.addItem("llapo");

                break;
                default:
                    cbox_distrito.removeAllItems();
                    break;
        }
        }
    }//GEN-LAST:event_cbox_provinciasKeyPressed

