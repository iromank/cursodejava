    private void cbox_regionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbox_regionActionPerformed
        String region = cbox_region.getSelectedItem().toString();
        switch (region) {

            case "lima":
                cbox_provincias.removeAllItems();
                cbox_provincias.addItem("callao");
                cbox_provincias.addItem("san borja");
                cbox_provincias.addItem("miraflores");
                break;
            case "ancash":
                cbox_provincias.removeAllItems();
                cbox_provincias.addItem("corongo");
                cbox_provincias.addItem("pallasca");
                cbox_provincias.addItem("santa");
                break;
            case "tacna":
                cbox_provincias.removeAllItems();
                cbox_provincias.addItem("nose");
                cbox_provincias.addItem("nose");
                cbox_provincias.addItem("nose");
                cbox_provincias.addItem("nose");
                break;
            case "ica":
                
                cbox_provincias.removeAllItems();
                cbox_provincias.addItem("nose");
                cbox_provincias.addItem("nose");
                cbox_provincias.addItem("nose");
                cbox_provincias.addItem("nose");
                break;
  default:
                    cbox_provincias.removeAllItems();
                    break;
        }
        

    }//GEN-LAST:event_cbox_regionActionPerformed

